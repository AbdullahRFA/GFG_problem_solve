class Solution:
    def maxSubArraySum(self, arr):
        # Your code here
        current_sum = max_sum = arr[0]
        for x in range(1, len(arr)):
            current_sum = max(arr[x], current_sum+arr[x])
            max_sum = max(max_sum, current_sum)
        return max_sum


